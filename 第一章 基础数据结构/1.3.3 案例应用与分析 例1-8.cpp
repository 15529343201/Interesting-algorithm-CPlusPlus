/*
	例1-8 设有一个顺序栈S,元素s1,s2,s3,s4,s5,s6依次入栈，
如果6个元素的出栈顺序为s2,s3,s4,s6,s5,s1,则顺序栈的容量
至少为多少?
	分析:栈结构的特点是先进先出(FILO),所以我们要判断栈
的容量的最小值,就是要考虑在最小的空间下按照栈的先进后出
原则,能够输出的元素顺序。由于入栈操作和出栈操作可以是
交叉进行的,所以该题我们可以按照题目所给的出栈顺序和入栈
顺序综合考虑。题目中入栈是采用的顺序入栈,即s1在s2前入栈,
s2在s3前面入栈,以此类推。
	(1)第一个输出的元素是s2,根据入栈顺序,此时已经入栈的
元素肯定为s1,s2,此时栈的容量至少为2.
	(2)第二个输出的元素为s3，如果容量为2，并且第一个元素
s2已经出栈，那么s3可以入栈并立即出栈,此时栈的容量依然为2。
	(3)s4出栈,栈的容量依然可以是2。
	(4)第4个出栈元素是s6，由于s5在s6前面入栈，所以s6出栈
前栈中元素必须包含s1,s5和s6,此时来看栈的容量至少为3.
	(5)s5和s1直接出栈。
	综上所述，该栈的容量应该是3.
*/ 
